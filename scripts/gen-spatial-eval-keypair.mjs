#!/usr/bin/env node
/**
 * One-shot generator for the RRF spatial-eval ML-DSA-65 keypair.
 *
 * - Generates a fresh keypair via rcan-ts.
 * - Writes the public key to functions/v1/spatial-eval/_lib/rrf_pubkey.json,
 *   keyed by the spec version passed on argv (default: 1.0.0). This file
 *   is committed; pubkeys are public.
 * - Prints the private key to stdout exactly once. The private key is NEVER
 *   committed. Pipe it directly into `wrangler secret put RRF_SPATIAL_EVAL_PQ_PRIV`
 *   so it lands in Cloudflare's encrypted secrets store.
 *
 * Usage:
 *   node scripts/gen-spatial-eval-keypair.mjs [spec_version]
 *
 * Then:
 *   wrangler secret put RRF_SPATIAL_EVAL_PQ_PRIV   # paste the printed value
 */

import { generateMlDsaKeypair } from "rcan-ts";
import { writeFileSync, readFileSync, existsSync } from "node:fs";
import { dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { mkdirSync } from "node:fs";

const SPEC_VERSION = process.argv[2] || "1.0.0";
const FORCE = process.argv.includes("--force");

const ROOT = dirname(dirname(fileURLToPath(import.meta.url)));
const PUBKEY_PATH = `${ROOT}/functions/v1/spatial-eval/_lib/rrf_pubkey.ts`;
const HEADER = `// Generated by scripts/gen-spatial-eval-keypair.mjs. Pubkeys are public;
// add a new spec_version entry by re-running the script with that version.
// Never edit by hand or commit a private key alongside.
//
// Loaded as a static TS module so workerd doesn't rely on JSON import
// attributes (which aren't a stable Cloudflare Pages Functions feature yet).

export interface PubkeyEntry {
  alg: string;
  pubkey: string;
  generated_at: string;
}

export const RRF_SPATIAL_EVAL_PUBKEYS: Record<string, PubkeyEntry> = `;

function b64(bytes) {
  return Buffer.from(bytes).toString("base64");
}

function readExistingRegistry(path) {
  if (!existsSync(path)) return {};
  const src = readFileSync(path, "utf-8");
  // Extract the JSON literal between the `= ` and the trailing `;`. Brittle
  // by design — we only ever read what this same script wrote.
  const m = src.match(/=\s*({[\s\S]*});?\s*$/);
  if (!m) return {};
  return JSON.parse(m[1]);
}

const kp = generateMlDsaKeypair();
const pub_b64 = b64(kp.publicKey);
const priv_b64 = b64(kp.privateKey);

const registry = readExistingRegistry(PUBKEY_PATH);

if (registry[SPEC_VERSION] && !FORCE) {
  console.error(
    `error: rrf_pubkey.ts already has an entry for spec_version=${SPEC_VERSION}.`,
  );
  console.error(
    `Refusing to overwrite — rotation requires a minor version bump.`,
  );
  console.error(`Pass --force to rotate within the same version anyway (debug only).`);
  process.exit(2);
}

registry[SPEC_VERSION] = {
  alg: "ml-dsa-65",
  pubkey: pub_b64,
  generated_at: new Date().toISOString(),
};

mkdirSync(dirname(PUBKEY_PATH), { recursive: true });
writeFileSync(PUBKEY_PATH, HEADER + JSON.stringify(registry, null, 2) + ";\n");

console.error(`✓ wrote pubkey to ${PUBKEY_PATH} (spec_version=${SPEC_VERSION})`);
console.error("");
console.error("Set the private key as a Cloudflare secret. Run:");
console.error("");
console.error("    wrangler secret put RRF_SPATIAL_EVAL_PQ_PRIV");
console.error("");
console.error("And paste the value below when prompted (one line):");
console.error("");
// Use process.stdout.write — NO trailing newline — so a piped consumer
// (e.g. `node gen-... | wrangler secret put NAME`) gets the bare base64
// payload. atob() on the Cloudflare side rejects whitespace.
process.stdout.write(priv_b64);
